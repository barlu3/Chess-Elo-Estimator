long long PST::pstValue(char pieceType, bool isWhite, int row, int col) {
    char type = std::toupper(static_cast<unsigned char>(pieceType));

    int tableRow = isWhite ? row : (7 - row);

    switch (type) {
        case 'P': return pawnTable[tableRow][col];
        case 'N': return knightTable[tableRow][col];
        case 'B': return bishopTable[tableRow][col];
        case 'R': return rookTable[tableRow][col];
        case 'Q': return queenTable[tableRow][col];
        case 'K': return kingTable[tableRow][col];
        default: return 0;
    }
}